class ASPP(nn.Module):
    def __init__(self, in_channels, out_channels, dilate_rates=[6, 12, 18]):
        super(ASPP, self).__init__()

        self.aspp1_offset_net = nn.Conv2d(in_channels, 18, kernel_size=1, padding=0, dilation=1)
        self.aspp1 = DeformConv2d(in_channels, out_channels, kernel_size=3, padding=1, dilation=1)

        self.aspp2_offset_net = nn.Conv2d(in_channels, 18, kernel_size=3, padding=dilate_rates[0],
                                          dilation=dilate_rates[0])
        self.aspp2 = DeformConv2d(in_channels, out_channels, kernel_size=3, padding=dilate_rates[0],
                                  dilation=dilate_rates[0])

        self.aspp3_offset_net = nn.Conv2d(in_channels, 18, kernel_size=3, padding=dilate_rates[1],
                                          dilation=dilate_rates[1])
        self.aspp3 = DeformConv2d(in_channels, out_channels, kernel_size=3, padding=dilate_rates[1],
                                  dilation=dilate_rates[1])

        self.aspp4_offset_net = nn.Conv2d(in_channels, 18, kernel_size=3, padding=dilate_rates[2],
                                          dilation=dilate_rates[2])
        self.aspp4 = DeformConv2d(in_channels, out_channels, kernel_size=3, padding=dilate_rates[2],
                                  dilation=dilate_rates[2])

        self.global_avg_pool = nn.Sequential(nn.AdaptiveAvgPool2d((1, 1)),
                                             nn.Conv2d(in_channels, out_channels, 1, stride=1, bias=False),
                                             nn.BatchNorm2d(out_channels),
                                             nn.ReLU())
        self.conv1 = nn.Conv2d(out_channels * 5, out_channels, 1, bias=False)
        self.bn1 = nn.BatchNorm2d(out_channels)
        self.relu = nn.ReLU()
        self.dropout = nn.Dropout(0.5)

    def forward(self, x):
        x1_offset = self.aspp1_offset_net(x)
        x1 = self.aspp1(x, x1_offset)

        x2_offset = self.aspp2_offset_net(x)
        x2 = self.aspp2(x, x2_offset)

        x3_offset = self.aspp3_offset_net(x)
        x3 = self.aspp3(x, x3_offset)

        x4_offset = self.aspp4_offset_net(x)
        x4 = self.aspp4(x, x4_offset)

        x5 = self.global_avg_pool(x)
        x5 = F.interpolate(x5, size=x4.size()[2:], mode='bilinear', align_corners=True)
        x = torch.cat((x1, x2, x3, x4, x5), dim=1)

        x = self.conv1(x)
        x = self.bn1(x)
        x = self.relu(x)

        return self.dropout(x)
class FeatureExtractor(nn.Module):
    def __init__(self, in_channels, out_channels, reduction=4):
        super(FeatureExtractor, self).__init__()
        self.attention = nn.Sequential(
            nn.Conv2d(in_channels, out_channels, kernel_size=1, bias=False),
            nn.ReLU(inplace=True),
            nn.Conv2d(out_channels, out_channels, kernel_size=3, padding=1, bias=False),
            nn.ReLU(inplace=True)
        )
        self.non_attention = nn.Sequential(
            nn.Conv2d(in_channels, out_channels, kernel_size=3, padding=1, bias=False),
            nn.ReLU(inplace=True)
        )
        self.attention_weights = nn.Sequential(
            nn.AdaptiveAvgPool2d(1),
            nn.Conv2d(out_channels, out_channels // reduction, kernel_size=1, bias=False),
            nn.ReLU(inplace=True),
            nn.Conv2d(out_channels // reduction, 1, kernel_size=1, bias=False),
            nn.Sigmoid()
        )
        self.lrelu = nn.LeakyReLU(negative_slope=0.2, inplace=True)

    def forward(self, x):
        residual = x

        attention_output = self.attention(x)
        non_attention_output = self.non_attention(x)

        attention_weights = self.attention_weights(attention_output)
        x = attention_output * attention_weights + non_attention_output

        x = self.lrelu(x)

        out = x + residual

        return out
class DeepLab(nn.Module):
    def __init__(self, num_classes, backbone="mobilenet", pretrained=True, downsample_factor=16):
        super(DeepLab, self).__init__()
        if backbone == "xception":
            # ----------------------------------#
            #   Obtain two feature layers
            #   shallow feature    [128,128,256]
            #   core    [30,30,2048]
            # ----------------------------------#
            self.backbone = xception(downsample_factor=downsample_factor, pretrained=pretrained)
            in_channels = 2048
            low_level_channels = 256
        elif backbone == "mobilenet":
            # ----------------------------------#
            #   Obtain two feature layers
            #   shallow feature    [128,128,24]
            #   core    [30,30,320]
            # ----------------------------------#
            self.backbone = MobileNetV2(downsample_factor=downsample_factor, pretrained=pretrained)
            in_channels = 320
            low_level_channels = 24
        else:
            raise ValueError('Unsupported backbone - `{}`, Use mobilenet, xception.'.format(backbone))

        # -----------------------------------------#
        #   ASPP Feature Extraction Module
        #   Feature Extraction Using Expansion Convolution with Different Expansion Rates
        # -----------------------------------------#
        self.aspp = ASPP(in_channels, 256)
        self.att1 = FeatureExtractor(low_level_channels, low_level_channels)
        self.att2 = FeatureExtractor(256, 256)
        # ----------------------------------#
        #   Shallow feature edge
        # ----------------------------------#
        self.shortcut_conv = nn.Sequential(
            nn.Conv2d(low_level_channels, 48, 1),
            nn.BatchNorm2d(48),
            nn.ReLU(inplace=True)
        )

        self.cat_conv = nn.Sequential(
            nn.Conv2d(48 + 256, 256, 3, stride=1, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(inplace=True),
            nn.Dropout(0.5),

            nn.Conv2d(256, 256, 3, stride=1, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(inplace=True),

            nn.Dropout(0.1),
        )
        self.cls_conv = nn.Conv2d(256, num_classes, 1, stride=1)

    def forward(self, x):
        H, W = x.size(2), x.size(3)
        # -----------------------------------------#
        # Get two feature layers
        # low_level_features: shallow features-convolution processed
        # x : backbone - enhanced feature extraction using ASPP structure
        # -----------------------------------------#
        low_level_features, x = self.backbone(x)
        x = self.aspp(x)
        low_level_features = self.shortcut_conv(low_level_features)

        # -----------------------------------------#
        # Up-sample enhanced feature edges
        # Stack with shallow features and then use convolution for feature extraction
        # -----------------------------------------#
        x = F.interpolate(x, size=(low_level_features.size(2), low_level_features.size(3)), mode='bilinear',
                          align_corners=True)  # 256, 128,128
        x = self.att2(x)
        x = self.cat_conv(torch.cat((x, low_level_features), dim=1))
        x = self.cls_conv(x)
        x = F.interpolate(x, size=(H, W), mode='bilinear', align_corners=True)
        return x
