class UNet_2Plus(nn.Module):
    def __init__(self, img_ch,num_classes):
        super().__init__()
        # channel number
        nb_filter = [32, 64, 128, 256, 512]
        # Maximum pooling layer
        self.pool = nn.MaxPool2d(2, 2)
        # upper sampling layer
        self.up = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
        # Coding the first convolution
        self.conv0_0 = Residual_block(img_ch, nb_filter[0])
        self.ca1 = CoordAtt(nb_filter[0], nb_filter[0])
        # Coding the second convolution
        self.conv1_0 = Residual_block(nb_filter[0], nb_filter[1])
        self.ca2 = CoordAtt(nb_filter[1], nb_filter[1])
        # Coding the third convolution
        self.conv2_0 = Residual_block(nb_filter[1], nb_filter[2])
        self.ca3 = CoordAtt(nb_filter[2], nb_filter[2])
        #  Coding the 4th convolution
        self.conv3_0 = Residual_block(nb_filter[2], nb_filter[3])
        self.ca4 = CoordAtt(nb_filter[3], nb_filter[3])
        # Coding the fifth convolution
        self.conv4_0 = Residual_block(nb_filter[3], nb_filter[4])
        self.ca5 = CoordAtt(nb_filter[4], nb_filter[4])
        # First convolution in the middle of the first layer
        self.conv0_1 = DoubleConv(nb_filter[0] + nb_filter[1], nb_filter[0])
        # Second convolution in the middle of the first layer
        self.conv1_1 = DoubleConv(nb_filter[1] + nb_filter[2], nb_filter[1])
        # Third convolution in the middle of the first layer
        self.conv2_1 = DoubleConv(nb_filter[2] + nb_filter[3], nb_filter[2])
        # Decode the first convolution
        self.conv3_1 = DoubleConv(nb_filter[3] + nb_filter[4], nb_filter[3])
        # First convolution in the middle of the second layer
        self.conv0_2 = DoubleConv(nb_filter[0] * 2 + nb_filter[1], nb_filter[0])
        # Second convolution in the middle of the second layer
        self.conv1_2 = DoubleConv(nb_filter[1] * 2 + nb_filter[2], nb_filter[1])
        # Decode the second convolution
        self.conv2_2 = DoubleConv(nb_filter[2] * 2 + nb_filter[3], nb_filter[2])
        # First convolution in the middle of the third layer
        self.conv0_3 = DoubleConv(nb_filter[0] * 3 + nb_filter[1], nb_filter[0])
        # Decode the third convolution
        self.conv1_3 = DoubleConv(nb_filter[1] * 3 + nb_filter[2], nb_filter[1])
        # First convolution in the middle of the fourth layer
        self.conv0_4 = DoubleConv(nb_filter[0] * 4 + nb_filter[1], nb_filter[0])
        # Classification function
        self.sigmoid = nn.Sigmoid()
        # Final output function
        self.final = nn.Conv2d(nb_filter[0], num_classes, kernel_size=1)


    def forward(self, input):
        x0_0 = self.conv0_0(input)
        x0_0 = self.ca1(x0_0)
        x1_0 = self.conv1_0(self.pool(x0_0))
        x1_0 = self.ca2(x1_0)
        x0_1 = self.conv0_1(torch.cat([x0_0, self.up(x1_0)], 1))

        x2_0 = self.conv2_0(self.pool(x1_0))
        x2_0 = self.ca3(x2_0)
        x1_1 = self.conv1_1(torch.cat([x1_0, self.up(x2_0)], 1))
        x0_2 = self.conv0_2(torch.cat([x0_0, x0_1, self.up(x1_1)], 1))

        x3_0 = self.conv3_0(self.pool(x2_0))
        x3_0 = self.ca4(x3_0)
        x2_1 = self.conv2_1(torch.cat([x2_0, self.up(x3_0)], 1))
        x1_2 = self.conv1_2(torch.cat([x1_0, x1_1, self.up(x2_1)], 1))
        x0_3 = self.conv0_3(torch.cat([x0_0, x0_1, x0_2, self.up(x1_2)], 1))

        x4_0 = self.conv4_0(self.pool(x3_0))
        x4_0 = self.ca5(x4_0)
        x3_1 = self.conv3_1(torch.cat([x3_0, self.up(x4_0)], 1))
        x2_2 = self.conv2_2(torch.cat([x2_0, x2_1, self.up(x3_1)], 1))
        x1_3 = self.conv1_3(torch.cat([x1_0, x1_1, x1_2, self.up(x2_2)], 1))
        x0_4 = self.conv0_4(torch.cat([x0_0, x0_1, x0_2, x0_3, self.up(x1_3)], 1))

        output = self.final(x0_4)
        output = self.sigmoid(output)
        return output
